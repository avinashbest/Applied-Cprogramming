//Integer Arithmetic Operations
#include <stdio.h>

int main(void)
{
    int x = -3, y = +4; //Unary Operators
    printf("\n%d\t%d\n\n", x, y);

    int a = 17, b = 4;

    printf("Sum = %d\n", a + b);        //Binary Operator +
    printf("Difference = %d\n", a - b); //Binary Operator -
    printf("Product = %d\n", a * b);    //Binary Operator *
    printf("Quotient = %d\n", a / b);   //Binary Operator /
    printf("Remainder = %d\n", a % b);  //Binary Operator %

    return 0;
}
